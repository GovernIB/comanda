const useTreeDataEntornAppRenderCell = (apps?: AppModel[]) => {
    return React.useCallback((params: any) => {
        const app = apps?.find((app) => app.id === parseInt(params.formattedValue));
        if (typeof params.id === 'number' || app == null) {
            return <GridTreeDataGroupingCell {...params} />;
        }
        return <GridTreeDataGroupingCell
            {...params}
            formattedValue={
                <Box
                    sx={{
                        display: 'flex',
                        alignItems: 'center',
                        gap: '8px',
                    }}>
                    {app.logo && <img
                        src={'data:image/png;base64,' + app.logo}
                        alt="Logo"
                        style={{ height: '48px' }}/>}
                    {app.nom}
                </Box>
            }
        />;
    }, [apps]);
}

const AppDataTable: React.FC<{
    salutLastItems: SalutModel[];
    apps?: AppModel[];
    entorns?: EntornModel[];
    entornApps: EntornAppModel[];
    groupedApp?: AppModel;
    groupedEntorn?: EntornModel;
    isGroupingDisabled: boolean;
}> = React.memo((props) => {
    const { salutLastItems, apps, entorns, entornApps, groupedEntorn, isGroupingDisabled } = props;
    const { t } = useTranslation();
    const dataGridLocale = useDataGridLocale();
    const { getLinkComponent } = useBaseAppContext();
    const gridApiRef = useGridApiRef();
    const [currentOpenActiveAlarmsId, setCurrentOpenActiveAlarmsId] = React.useState<string | number>();
    const [currentOpenConfigAlarmsId, setCurrentOpenConfigAlarmsId] = React.useState<string | number>();

    const treeDataRenderCell =  useTreeDataEntornAppRenderCell(apps);
    const getTreeDataPath = React.useCallback(
        (row: any) => [row.app.id, row.entorn.description],
        []
    );
    const groupingColDefAdditionalProps = React.useMemo(
        () => ({ renderCell: treeDataRenderCell, minWidth: 265 }),
        [treeDataRenderCell]
    );
    const { dataGridProps: treeDataGridProps } = useTreeDataWithoutSwitch(
        getTreeDataPath,
        gridApiRef,
        t($ => $.page.salut.apps.column.group),
        0.7,
        true,
        isGroupingDisabled,
        groupingColDefAdditionalProps);

    const findSalutItem: (id: GridRowId) => SalutModel | null = React.useCallback(
        (id: GridRowId) => {
            return salutLastItems?.find((entry: SalutModel) => entry.entornAppId === id) ?? null;
        },
        [salutLastItems]
    );

    const renderItemStateChip = React.useCallback(
        (id: GridRowId, salutField: SalutField.APP_ESTAT | SalutField.BD_ESTAT) => {
            const salutItem: SalutModel | null = findSalutItem(id);
            if (salutItem == null) {
                return undefined;
            }
            return (
                <ItemStateChip
                    sx={{ mr: 1 }}
                    salutField={salutField}
                    salutStatEnum={salutItem[salutField] as SalutEstatEnum}
                />
            );
        },
        [findSalutItem]
    );

    const columns = React.useMemo(() => {
        const rawColumns: (MuiDataGridColDef | null)[] = [
            !isGroupingDisabled
                ? {
                      flex: 0.5,
                      field: 'nomLogo',
                      headerName: t($ => $.page.salut.apps.column.group),
                      minWidth: 150,
                      valueGetter: (_value, row) => {
                          if (isAutogeneratedRow(row)) return;
                          const entorn =
                              entorns != null &&
                              entorns.find(entorn => entorn.id === row.entorn.id);
                          const app = apps != null && apps.find(app => app.id === row.app.id);
                          if (!entorn || !app) return;

                          const isGroupedByEntorn = groupedEntorn != null;
                          if (isGroupedByEntorn) return app.nom;
                          return entorn.nom ?? entorn.codi;
                      },
                      renderCell: params => {
                          if (isAutogeneratedRow(params.row)) return;
                          const isGroupedByEntorn = groupedEntorn != null;

                          const app =
                              apps != null && apps.find(app => app.id === params.row.app.id);
                          if (isGroupedByEntorn && app)
                              return (
                                  <Box
                                      sx={{
                                          display: 'flex',
                                          alignItems: 'center',
                                          gap: '8px',
                                      }}
                                  >
                                      {app.logo && (
                                          <img
                                              src={'data:image/png;base64,' + app.logo}
                                              alt="Logo"
                                              style={{ height: '48px' }}
                                          />
                                      )}
                                      {params.value}
                                  </Box>
                              );
                      },
                  }
                : null,
            {
                flex: 0.3,
                field: 'estat',
                headerName: t($ => $.page.salut.apps.column.estat),
                minWidth: 100,
                renderCell: ({ id }) => renderItemStateChip(id, SalutField.APP_ESTAT),
            },
            {
                flex: 0.3,
                field: 'infoData',
                headerName: t($ => $.page.salut.apps.column.infoData),
                description: t($ => $.page.salut.apps.column.infoDataDescription),
                minWidth: 160,
                renderCell: ({ id }) => {
                    const salutItem: SalutModel | null = findSalutItem(id);
                    if (salutItem == null) {
                        return '';
                    }
                    return salutItem?.data
                        ? dateFormatLocale(salutItem?.data, true)
                        : t($ => $.page.salut.nd);
                },
            },
            {
                flex: 0.1,
                field: 'versio',
                headerName: t($ => $.page.salut.apps.column.versio),
                minWidth: 90,
            },
            {
                flex: 0.05,
                field: 'revisioSimplificat',
                headerName: t($ => $.page.salut.apps.column.revisio),
                minWidth: 100,
            },
            {
                flex: 0.3,
                field: 'bd',
                headerName: t($ => $.page.salut.apps.column.bd),
                minWidth: 130,
                renderCell: ({ id }) => renderItemStateChip(id, SalutField.BD_ESTAT),
            },
            {
                flex: 0.1,
                field: 'latencia',
                headerName: t($ => $.page.salut.apps.column.latencia),
                minWidth: 80,
                valueGetter: (_value, row) => {
                    const salutItem: SalutModel | null = findSalutItem(row.id);
                    if (salutItem == null) {
                        return '';
                    }
                    return salutItem?.appLatencia != null
                        ? salutItem.appLatencia + ' ms'
                        : t($ => $.page.salut.nd);
                },
            },
            {
                flex: 0.1,
                field: SalutModel.INTEGRACIONS,
                headerName: t($ => $.page.salut.apps.column.integ),
                minWidth: 140,
                renderCell: ({ id }) => {
                    const salutItem: SalutModel | null = findSalutItem(id);

                    if (!salutItem) {
                        return null;
                    }

                    return <SalutIntegracionsChips salutItem={salutItem} />;
                },
            },
            {
                flex: 0.2,
                field: SalutModel.SUBSISTEMES,
                headerName: t($ => $.page.salut.apps.column.subsis),
                minWidth: 140,
                renderCell: ({ id }) => {
                    const salutItem: SalutModel | null = findSalutItem(id);

                    if (!salutItem) {
                        return null;
                    }
                    return <SalutSubsistemesChips salutItem={salutItem} />;
                },
            },
            {
                flex: 0.1,
                field: 'msgs',
                headerName: t($ => $.page.salut.apps.column.msgs),
                minWidth: 120,
                renderCell: ({ id }) => {
                    const salutItem: SalutModel | null = findSalutItem(id);

                    if (!salutItem) {
                        return null;
                    }
                    return <SalutMissatgesChips salutItem={salutItem} />;
                },
            },
            {
                field: 'detalls',
                headerName: '',
                width: 160,
                renderCell: params =>
                    params.rowNode.type !== 'group' && (
                        <Box sx={{ height: '100%', width: '100%', display: 'flex', alignItems: 'center', justifyContent: 'center'}}>
                            <Button
                                variant="contained"
                                size="small"
                                component={getLinkComponent()}
                                to={'appinfo/' + params.id}
                            >
                                {t($ => $.page.salut.apps.detalls)}
                            </Button>
                            <AlarmsButton
                                filterBy={{ entornAppId: params.id }}
                                onClick={() => setCurrentOpenActiveAlarmsId(params.id)}
                            />
                            <IconButton
                                onClick={() => setCurrentOpenConfigAlarmsId(params.id)}
                                title={t($ => $.page.salut.alarmes.configAlarmesTitle)}
                                size="small"
                            >
                                <Icon>tune</Icon>
                            </IconButton>
                        </Box>
                    ),
            },
        ];
        return rawColumns.filter(column => column !== null);
    }, [
        apps,
        entorns,
        findSalutItem,
        getLinkComponent,
        groupedEntorn,
        isGroupingDisabled,
        renderItemStateChip,
        t,
    ]);

    return (
        <>
            <AlarmsDialog
                open={currentOpenActiveAlarmsId != null}
                setOpen={isOpen =>
                    setCurrentOpenActiveAlarmsId(isOpen ? currentOpenActiveAlarmsId : undefined)
                }
                filterBy={{ entornAppId: currentOpenActiveAlarmsId }}
            />
            <AlarmaConfigDialog
                open={currentOpenConfigAlarmsId != null}
                setOpen={isOpen =>
                    setCurrentOpenConfigAlarmsId(isOpen ? currentOpenConfigAlarmsId : undefined)
                }
                filterBy={{ entornAppId: currentOpenConfigAlarmsId }}
            />
            <DataGridPro
                apiRef={gridApiRef}
                columns={columns}
                rows={entornApps}
                localeText={dataGridLocale}
                hideFooter
                slots={{
                    noRowsOverlay: DataGridNoRowsOverlay as GridSlots['noRowsOverlay'],
                }}
                {...treeDataGridProps}
            />
        </>
    );
});

export const SalutWidgetTitle: React.FC<{
    app?: AppModel;
    entorn?: EntornModel;
    loading?: boolean;
    midaFontTitol?: number;
}> = ({ app, entorn, loading, midaFontTitol }) => {
    const { t } = useTranslation();
    const titleEstils = {
        ...estils.titleText,
        fontSize: midaFontTitol ? `${midaFontTitol}px` : estils.titleText.fontSize,
    };

    const titleText = app?.nom || entorn?.nom || t($ => $.page.salut.groupingSelect.titleNoGrouping);

    return (
        <Box>
            {loading ? (
                <>
                    <Skeleton width="70%" height={32} />
                    <Box sx={estils.iconContainer}>
                        <Skeleton width={40} height={24} />
                    </Box>
                </>
            ) : (
                <>
                    {
                        <Box
                            sx={{
                                display: 'flex',
                                alignItems: 'center',
                                gap: '8px',
                                height: '38px',
                            }}
                        >
                            {app?.logo && (
                                <img
                                    src={'data:image/png;base64,' + app.logo}
                                    alt={'Logo ' + app.nom}
                                    style={{ height: '100%' }}
                                />
                            )}
                            <Typography sx={titleEstils}>{titleText}</Typography>
                        </Box>
                    }
                </>
            )}
        </Box>
    );
};

export const SalutWidgetContent: React.FC<{
    expanded: boolean;
    setExpanded: (expanded: boolean) => void;
    parentContainerFullWidth: number | null;
    isGroupingDisabled: boolean;
    salutLastItems: SalutModel[];
    agrupacio: string;
    estats: SalutData['estats'];
    loading?: boolean;
    groupedApp?: AppModel;
    groupedEntorn?: EntornModel;
    apps?: AppModel[];
    entorns?: EntornModel[];
    entornApps: EntornAppModel[];
    grupsDates: string[];
}> = ({
    expanded: expandedProp,
    setExpanded,
    parentContainerFullWidth,
    isGroupingDisabled,
    salutLastItems,
    agrupacio,
    estats,
    loading,
    groupedApp,
    groupedEntorn,
    entornApps,
    apps,
    entorns,
    grupsDates,
}) => {
    const theme = useTheme();
    const isScreenSmall = useMediaQuery(theme.breakpoints.down('lg'));
    const { size: trackedGridSize, refCallback: trackedGridRef } = useSizeTracker(100);
    const expanded = !isGroupingDisabled ? expandedProp : true;

    if (loading)
        return (
            <>
                <Box sx={{ ...estils.contentText(true), width: '10em' }}>
                    <Skeleton width="100%" height={80} />
                </Box>
                <Box sx={{ ...estils.contentText(true), width: '4em' }}>
                    <Skeleton width="100%" height={20} />
                </Box>
            </>
        );

    return (
        <Paper
            elevation={1}
            sx={{
                height: !expanded ? '230px' : trackedGridSize?.height + 'px',
                width: !expanded ? '400px' : '100%',
                transition: 'height 0.3s ease-in-out, width 0.3s ease-in-out',
                overflow: 'hidden',
                position: 'relative',
            }}
        >
            <Box
                sx={{
                    width: !expanded ? undefined : parentContainerFullWidth + 'px', // Evita que los gráficos usen el ancho interpolado del transition, saltan directamente a la anchura entera.
                    px: 2,
                    pt: 1,
                }}
                ref={trackedGridRef}
            >
                <Box
                    sx={{
                        display: 'flex',
                        flexDirection: 'column',
                        width: '100%',
                    }}
                >
                    <SalutWidgetTitle app={groupedApp} entorn={groupedEntorn} />
                    <Box
                        sx={{
                            display: 'flex',
                            flexDirection: isScreenSmall ? 'column' : 'row',
                            gap: isScreenSmall ? 2 : 0,
                            mb: 1,
                        }}
                    >
                        <Box
                            sx={{
                                minHeight: 0,
                                display: 'flex',
                                alignItems: 'center',
                                justifyContent: 'center',
                                height: '170px',
                                width: !isScreenSmall ? '340px' : '100%',
                                flexShrink: 0, // Evita que el PieChart "pegue un salto" al expandir el Widget
                            }}
                        >
                            <ErrorBoundary fallback={<SalutErrorBoundaryFallback />}>
                                <UpdownPieChart salutLastItems={salutLastItems} />
                            </ErrorBoundary>
                        </Box>
                        <React.Activity mode={expanded ? 'visible' : 'hidden'}>
                            <Box sx={{ height: '200px', flexGrow: 1 }}>
                                <ErrorBoundary fallback={<SalutErrorBoundaryFallback />}>
                                    <UpdownBarChart
                                        agrupacio={agrupacio}
                                        estats={estats}
                                        grupsDates={grupsDates}
                                    />
                                </ErrorBoundary>
                            </Box>
                        </React.Activity>

                    </Box>
                </Box>
                <Box sx={{ width: '100%' }}>
                    <React.Activity mode={expanded ? 'visible' : 'hidden'}>
                        <AppDataTable
                            groupedApp={groupedApp}
                            groupedEntorn={groupedEntorn}
                            isGroupingDisabled={isGroupingDisabled}
                            apps={apps}
                            entorns={entorns}
                            entornApps={entornApps}
                            salutLastItems={salutLastItems}
                        />
                    </React.Activity>
                </Box>
                {/* Botón de expansión duplicado para ocupar la altura del botón original con position: absolute */}
                <Box sx={{ width: '100%', mt: 1 }}>
                    <IconButton disabled>
                        <KeyboardArrowUpIcon
                            sx={{
                                visibility: 'hidden',
                            }}
                        />
                    </IconButton>
                </Box>
            </Box>
            {!isGroupingDisabled && (
                <Box
                    sx={{
                        display: 'flex',
                        justifyContent: 'center',
                        position: 'absolute',
                        bottom: '0px',
                        width: '100%',
                    }}
                >
                    <Button
                        sx={{ width: '100%', color: 'grey' }}
                        onClick={() => setExpanded(!expanded)}
                    >
                        {expanded ? <KeyboardArrowUpIcon /> : <KeyboardArrowDownIcon />}
                    </Button>
                </Box>
            )}
        </Paper>
    );
};

export const SalutLlistat = ({
    salutGroups,
    grupsDates,
    agrupacio,
    apps,
    entorns,
    setExpanded,
    isExpanded,
}: {
    apps?: AppModel[];
    entorns?: EntornModel[];
    salutGroups: SalutData[];
    grupsDates?: string[];
    agrupacio?: string;
    springFilter?: string;
    setExpanded: (expand: boolean, context: SalutData) => void;
    isExpanded: (salutGroup: SalutData) => boolean;
}) => {
    const { size: trackedGridSize, refCallback: trackedGridRef } = useSizeTracker(100);
    if (!salutGroups.length || grupsDates == null || agrupacio == null) return;

    return (
        <Box
            ref={trackedGridRef}
            sx={{
                display: 'flex',
                flexWrap: 'wrap',
                gap: 2,
                justifyContent: 'flex-start',
            }}
        >
            {salutGroups.map((salutGroup) => {
                // Si no se ha agrupado por app ni entorn, la agrupación debe estar desactivada
                const isGroupingDisabled =
                    salutGroup.groupedApp == null && salutGroup.groupedEntorn == null;
                return (
                        <SalutWidgetContent
                            key={`salutWidgetContent-${salutGroup.groupedApp?.id}-${salutGroup.groupedEntorn?.id}`}
                            expanded={isExpanded(salutGroup)}
                            setExpanded={expand => setExpanded(expand, salutGroup)}
                            parentContainerFullWidth={trackedGridSize?.width ?? null}
                            isGroupingDisabled={isGroupingDisabled}
                            salutLastItems={salutGroup.salutLastItems}
                            agrupacio={agrupacio}
                            estats={salutGroup.estats}
                            groupedApp={salutGroup.groupedApp}
                            groupedEntorn={salutGroup.groupedEntorn}
                            apps={apps}
                            entorns={entorns}
                            entornApps={salutGroup.entornApps}
                            grupsDates={grupsDates}
                        />
                );
            })}
        </Box>
    );
};
